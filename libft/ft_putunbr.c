#include "libft.h"

int	ft_putunbr(unsigned int n)
{
	int	count;

	count = 0;
	if (n >= 10)
	{
		count += ft_putunbr(n / 10);
		n = n % 10;
	}
	if (n < 10)
		count += ft_putchar(n + 48);
	return (count);
}
