#include "libft.h"

long long	ft_atoll(const char *nptr)
{
	int		i;
	int		sign;
	long long	res;

	i = 0;
	sign = 1;
	res = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = sign * (-1);
		i++;
	}
	while (ft_isdigit(nptr[i])) // y res esta dentro de los limites de INT
	{
		res = res * 10 + (nptr[i] - 48);
		i++;
	}
	return (sign * res);
}